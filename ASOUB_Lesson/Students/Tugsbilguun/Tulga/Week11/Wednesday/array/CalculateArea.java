public class CalculateArea
{
    private double pi = 3.14;
    public double CalculateArea ( int radius ) 
    {
        return pi * 2 * radius;
    }
    public double CalculatePerimeter ( int radius )
    {
        return pi * radius * radius;
    }
    
}

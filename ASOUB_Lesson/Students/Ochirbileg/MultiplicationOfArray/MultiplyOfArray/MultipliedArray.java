public class MultipliedArray
{
     public static void main(String[] args) 
    {
       int n=3;
       int [] intArray = new int [] {1,2,3,4,5,6,7};
       for(int i=0; i<intArray.length; i++) 
       {
           System.out.println(intArray[i]*n);
       }
    }
}
